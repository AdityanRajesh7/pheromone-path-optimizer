class Graph:
    def __init__(self, n):
        self.nvertices = n
        self.edges = {}
        self.weights = {}
        self.nodes_set = set()

    def get_nodes(self):
        return self.nodes_set

    def add_edge(self, node1, node2, weight):
        self.nodes_set.add(node1)
        self.nodes_set.add(node2)
        self.edges.setdefault(node1, []).append(node2)
        self.edges.setdefault(node2, []).append(node1)
        self.weights[(node1, node2)] = weight
        self.weights[(node2, node1)] = weight

    def neighbors(self, node):
        return self.edges.get(node, [])

    def has_edge(self, node1, node2):
        return node2 in self.edges.get(node1, [])

    def get_weight(self, node1, node2):
        return self.weights.get((node1, node2), float('inf'))

