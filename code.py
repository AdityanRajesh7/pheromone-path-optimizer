    robot1, = ax.plot([], [], 'go', markersize=10, label='Robot A')
    robot2, = ax.plot([], [], 'bo', markersize=10, label='Robot B')
    robot3, = ax.plot([], [], 'yo', markersize=10, label='Robot C')

    # Add path lines
    path_line1, = ax.plot([], [], 'g-', alpha=0.5)
    path_line2, = ax.plot([], [], 'b-', alpha=0.5)
    path_line3, = ax.plot([], [], 'm-', alpha=0.5)

    def update(frame):
        for robot, path, line in zip([robot1, robot2, robot3],
                                    collision_free_paths,
                                    [path_line1, path_line2, path_line3]):
            if path and frame < len(path):
                current_data = list(zip(*path[:frame + 1]))
                if current_data:
                    robot.set_data(*current_data)
                    line.set_data(*current_data)

        return robot1, robot2, robot3, path_line1, path_line2, path_line3

